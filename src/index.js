const MORSE_TABLE = {
  '.-': 'a',
  '-...': 'b',
  '-.-.': 'c',
  '-..': 'd',
  '.': 'e',
  '..-.': 'f',
  '--.': 'g',
  '....': 'h',
  '..': 'i',
  '.---': 'j',
  '-.-': 'k',
  '.-..': 'l',
  '--': 'm',
  '-.': 'n',
  '---': 'o',
  '.--.': 'p',
  '--.-': 'q',
  '.-.': 'r',
  '...': 's',
  '-': 't',
  '..-': 'u',
  '...-': 'v',
  '.--': 'w',
  '-..-': 'x',
  '-.--': 'y',
  '--..': 'z',
  '.----': '1',
  '..---': '2',
  '...--': '3',
  '....-': '4',
  '.....': '5',
  '-....': '6',
  '--...': '7',
  '---..': '8',
  '----.': '9',
  '-----': '0',
};

function decode(expr) {
  let exprArray = '';
  let result = [];
  exprArray = expr
    .replaceAll('**********', ' ')
    .replaceAll(/\d{10}/g, '$& ')
    .replaceAll('00', '')
    .replaceAll('10', '.')
    .replaceAll('11', '-');
  result = exprArray
    .split(' ')
    .map((x) => MORSE_TABLE[x] || ' ')
    .join('')
    .trim();
  return result;
}

